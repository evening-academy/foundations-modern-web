function findSquarePerimeter(side){
    return side*4;
}
console.log(findSquarePerimeter(7));

function findRectangleArea(length, width){
    return length * width;
}
console.log(findRectangleArea(4,5));

function findCubeArea(cubeEdgeLength){
    return 6 * cubeEdgeLength * cubeEdgeLength;
}
console.log(findCubeArea(4));

function findCubePerimeter(cubeEdgeLength){
    return cubeEdgeLength*12;
}
console.log(findCubePerimeter(4));



function findCircleArea(radiusCircle){
    return 3.14 * radiusCircle * radiusCircle;
}
console.log(findCircleArea(4));

function findCircleCircumference(radiusCircle){
    return 2 * 3.14 * radiusCircle;
}
console.log(findCircleCircumference(7));

function calculateSquareRoot(number){
    return Math.sqrt(number);
}
console.log(calculateSquareRoot(9))

function getRandomNumber(){
    return Math.random();
}
console.log(getRandomNumber(1));

